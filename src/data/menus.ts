export const CATEGORIES: Category[] = ['한식', '중식', '일식', '양식', '분식', '아시안'];
export const TAGS: Tag[] = ['매운 거', '면류'];

export function pickMenu(
  excludeCategories: Category[],
  excludeTags: Tag[],
  excludeName?: string
): MenuItem | null {
  // 카테고리별로 그룹핑
  const availableCategories = CATEGORIES.filter((cat) => !excludeCategories.includes(cat));
  if (availableCategories.length === 0) return null;

  // 카테고리 균등 선택 후 해당 카테고리 내에서 랜덤 (한식 편중 방지)
  const shuffledCats = [...availableCategories].sort(() => Math.random() - 0.5);

  for (const cat of shuffledCats) {
    const candidates = menus.filter(
      (m) =>
        m.category === cat &&
        !m.tags.some((t) => excludeTags.includes(t)) &&
        m.name !== excludeName
    );
    if (candidates.length > 0) {
      return candidates[Math.floor(Math.random() * candidates.length)];
    }
  }
  return null;
}

export interface WeekRecord {
  [category: string]: number;
}

export function getWeekRecord(): WeekRecord {
  try {
    const raw = localStorage.getItem('lunch-week-record');
    if (!raw) return {};
    const data = JSON.parse(raw) as { week: string; record: WeekRecord };
    const thisWeek = getWeekKey();
    if (data.week !== thisWeek) return {};
    return data.record;
  } catch {
    return {};
  }
}

export function addToWeekRecord(category: Category) {
  const record = getWeekRecord();
  record[category] = (record[category] || 0) + 1;
  localStorage.setItem(
    'lunch-week-record',
    JSON.stringify({ week: getWeekKey(), record })
  );
}

function getWeekKey(): string {
  const d = new Date();
  const day = d.getDay();
  const diff = d.getDate() - day + (day === 0 ? -6 : 1);
  const monday = new Date(d.setDate(diff));
  return monday.toISOString().slice(0, 10);
}
